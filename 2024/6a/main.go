type point struct{ x, y int }

func solution(data string) int {
	var seen = map[point]bool{}
	var dir int

	lines := convert(data)
	g := findGuard(lines)
	seen[g] = true

	for {
		switch check(lines, g, dir) {
		case ' ':
			return len(seen)
		case '#':
			dir = turn(dir)
		default:
			g = walk(g, dir)
			seen[g] = true
		}
	}
}

func check(data [][]byte, g point, dir int) byte {
	switch dir {
	case 0:
		g.y--
	case 1:
		g.x++
	case 2:
		g.y++
	case 3:
		g.x--
	}

	if g.x > len(data[0])-1 || g.x < 0 || g.y > len(data)-1 || g.y < 0 {
		return ' '
	}

	return data[g.y][g.x]
}

func walk(g point, dir int) point {
	switch dir {
	case 0:
		g.y--
	case 1:
		g.x++
	case 2:
		g.y++
	case 3:
		g.x--
	}

	return g
}

func turn(dir int) int {
	switch dir {
	case 3:
		dir = 0
	default:
		dir++
	}

	return dir
}

func findGuard(data [][]byte) point {
	for y, lines := range data {
		for x, char := range lines {
			if char == '^' {
				return point{x, y}
			}
		}
	}
	return point{}
}

func convert(data string) [][]byte {
	var res [][]byte

	lines := strings.Split(data, "\n")

	for _, line := range lines {
		res = append(res, []byte(line))
	}

	return res
}

